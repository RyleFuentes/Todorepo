    public function Auth(Request $request){
        $fetch = User::where('Email', $request->email)->first();

        if(!$fetch){
          return back()->with('fail', "We do not recognize your email");
        }else{
          if(Hash::check($request->password, $fetch->password)){
            //loggedUser is interchangeable this is where you will be the name of the session token the second value is the Value you will bring with that token
              $request->session()->put('loggedUser', $fetch->user_id);
              return redirect('dashboard')->with('message', 'Logged in successfully');
  
          }
          else{
              return back()->with('message','Incorrect Password');
          }
        }
    }

